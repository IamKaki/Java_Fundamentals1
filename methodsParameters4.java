public class methodsParameters4 {
    static void myMethod(String fname, int age){
        System.out.println(fname + " is " + age);
    }
    public static void main(String[] args){
        myMethod("John", 33);
        myMethod("Joy", 27);
        myMethod("Mathew", 22);
        myMethod("Fredrick", 18);
    }
}
